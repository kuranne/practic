local score = 55
if score >= 80 then
    print("A")
elseif score >= 75 then
    print("B+")
elseif score >= 70 then
    print("B")
elseif score >= 65 then
    print("C+")
elseif score >= 60 then
    print("C")
elseif score >= 55 then
    print("D+")
elseif score >= 50 then
    print("D")
else
    print("F")
end