#include <stdio.h>
void swap(int *a, int *b)
{
    int tmp = *a;
    *a = *b;
    *b = tmp;
}

int partition(int arr[], int low, int high)
{
    int right = arr[high];
    int lefti = low - 1;

    for (int j = low; j < high; j++)
    {
        if (arr[j] < right)
        {
            swap(&arr[j], &arr[++lefti]);
        }
    }

    swap(&arr[++lefti], &arr[high]);
    return lefti;
}

void quicksort(int arr[], int low, int high)
{
    if (low < high)
    {
        int pivoti = partition(arr, low, high);

        quicksort(arr, pivoti + 1, high);
        quicksort(arr, low, pivoti - 1);
    }
}

int binary_search(int arr[], int size, int target)
{
    int left = 0, right = size - 1, mid, result = -1;
    while (left <= right)
    {
        mid = (left + right) / 2;
        if (target == arr[mid])
        {
            result = mid;
            right = mid - 1;
        }
        else if (target < arr[mid])
            right = mid - 1;
        else
            left = mid + 1;
    }
    return result;
}

